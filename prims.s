bin_add:
    push rbp
    mov rbp, rsp

    mov r8, 0

    mov rsi, PVAR(0)
    push rsi
    push 1
    push SOB_NIL
    call is_float
    add rsp, 3*WORD_SIZE 


    cmp rax, SOB_TRUE
    je .test_next
    or r8, 1

.test_next:

    mov rsi, PVAR(1)
    push rsi
    push 1
    push SOB_NIL
    call is_float
    add rsp, 3*WORD_SIZE 


    cmp rax, SOB_TRUE
    je .load_numbers
    or r8, 2

