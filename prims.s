apply:
 mov r14,0               ;init the counter
 mov r13,[malloc_pointer];r13 points to the malloc pointer
 pop r15                 ;return addres     
 pop r9                  ;closure env
 pop r9                  ;arg count
 sub r9,2                ;args num without proc + list
 pop r12                 ;procedure

.arg_loop:
 cmp r9,0
 jle .exit_loop1
 shl r14,3
 pop qword[r13+r14]    ;pop arg i 
 shr r14,3
 add r14,1 
 sub r9,1
 jmp .arg_loop 

.exit_loop1: 
 pop r10               ;args list

.list_loop:
 cmp r10,SOB_NIL       ;finished list
 jne .cont1

.exit_loop2:
 mov r10,r14           ;save number of args 
 jmp .push_loop

 .cont1:
 CAR r11,r10
 CDR r10,r10
 shl r14,3
 mov [r13+r14],r11   ;mov arg j in the list to the vector
 shr r14,3
 add r14,1
 jmp .list_loop

.push_loop:
 cmp r14,0
 jne .cont2
 
 CLOSURE_ENV  r13,r12;r13 has proc env
 CLOSURE_CODE r9,r12 ;r9 points to proc code
 push r10            ;push arg count
 push r13            ;push rnv
 push r15            ;push ret addres
 jmp r9 

.cont2:
 sub r14,1
 shl r14,3
 push qword[r13+r14]
 shr r14,3  
 jmp .push_loop




